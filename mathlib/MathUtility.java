package com.bayamp.math.library;

public class MathUtility {
	public  static int addTwoIntNumbers(int num1,int num2) {
		int total = num1 + num2;
	    return total;
	 }
	public static int subTwoIntNumbers(int num1, int num2) {
		int difference = num1 - num2;
		return difference;

	}
	public static int mulTwoIntNumbers(int num1, int num2) {
		int product = num1 * num2;
		return product;

	}
	
		public static int modTwoIntNumbers(int num1, int num2) {
			int modulus = num1 % num2;
			return modulus;

		}

}
